// Current language state
let currentLanguage = localStorage.getItem('maheshLang') || 'en';

// Toggle language function
function toggleLanguage() {
    currentLanguage = currentLanguage === 'en' ? 'hi' : 'en';
    localStorage.setItem('maheshLang', currentLanguage);
    applyTranslations();
    updateLanguageButton();
}

// Update language button text
function updateLanguageButton() {
    const langText = document.getElementById('langText');
    const langBtn = document.getElementById('langToggle');
    
    if (langText) {
        langText.textContent = currentLanguage === 'en' ? 'हिंदी' : 'English';
    }
    
    if (langBtn) {
        langBtn.title = currentLanguage === 'en' ? 'Switch to Hindi' : 'Switch to English';
    }
}

// Apply translations to all elements with data-translate attribute
function applyTranslations() {
    const elements = document.querySelectorAll('[data-translate]');
    const lang = translations[currentLanguage];
    
    elements.forEach(element => {
        const key = element.getAttribute('data-translate');
        if (lang[key]) {
            element.innerHTML = lang[key];
        }
    });
    
    // Update search placeholder
    const searchInput = document.getElementById('searchInput');
    if (searchInput) {
        searchInput.placeholder = lang.search_placeholder || 'Search products...';
    }
    
    // Update navigation links
    const navLinks = document.querySelectorAll('.nav-links a');
    const navKeys = ['home', 'categories', 'products', 'services', 'about', 'contact'];
    navLinks.forEach((link, index) => {
        if (navKeys[index] && lang[navKeys[index]]) {
            link.textContent = lang[navKeys[index]];
        }
    });
}

// Initialize translations on page load
document.addEventListener('DOMContentLoaded', () => {
    updateLanguageButton();
    if (currentLanguage === 'hi') {
        applyTranslations();
    }
});
